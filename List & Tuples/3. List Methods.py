a = [1,5,6,2,4,9,0]

print(a)
a.sort()  # It will sort the list in ascending order
print(a)

a.reverse()  # It will sort the list in reverse order
print(a)

a.append(34) # It will add at the end of the list
print(a)

a.insert(5,67) # It will insert 67 at 5th index
print(a)

a.remove(67) # It will remove the element 67 from the lists
print(a)

a.pop(0) # It will remove the element at index 0 as specified
print(a)

